public extension String {
    public var first: String {
        return String(self[startIndex])
    }
}


extension NSManagedObject {
    func valueBool(forKey: String) -> Bool? {
        
        let fieldValue = self.value(forKey: forKey) as! String
        
        if fieldValue == nil {
            return false
        } else {
            return (fieldValue as! String).toBool()
        }

    }
    func valueInt(forKey: String) -> Int? {
        
        let fieldValue = self.value(forKey: forKey) as! String?
        
        return Int(fieldValue! as String)!
    }
}


extension String {
    func toBool() -> Bool? {
        switch self {
        case "True", "true", "yes", "1":
            return true
        case "False", "false", "no", "0":
            return false
        default:
            return nil
        }
    }
}

extension Dictionary {
    mutating func update(other:Dictionary) {
        for (key,value) in other {
